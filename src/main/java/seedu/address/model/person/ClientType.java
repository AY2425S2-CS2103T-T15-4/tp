package seedu.address.model.person;

/**
 * Represents the type of client in the address book.
 * A client can be a buyer, a seller, or unknown.
 */
public enum ClientType {
    /** Represents a buyer client. */
    BUYER,

    /** Represents a seller client. */
    SELLER,

    /** Represents a client whose type is unknown. */
    UNKNOWN
}

